/*Create a stored procedure to display supplier id, name, rating and Type_of_Service. For Type_of_Service, If rating =5, print “Excellent Service”,
If rating >4 print “Good Service”, If rating >2 print “Average Service” else print “Poor Service”.
*/
USE `ecommerce`;
DROP procedure IF EXISTS `ServiceType`;

USE `ecommerce`;
