/*Display the total number of customers based on gender who have placed orders of worth at least Rs.3000.*/
SELECT TEMP.CUS_GENDER,COUNT(TEMP.CUS_GENDER) AS TOTAL_CUSTOMERS FROM (
SELECT DISTINCT CUS_NAME,CUS_GENDER,O.ORD_AMOUNT 
FROM CUSTOMER C
INNER JOIN `ORDER` O ON O.CUS_ID = C.CUS_ID
WHERE O.ORD_AMOUNT >= 3000) TEMP
GROUP BY TEMP.CUS_GENDER;

/*Display all the orders along with product name ordered by a customer having Customer_Id=2*/
SELECT O.*,P.PRO_NAME
FROM `ORDER` O,PRODUCT P,SUPPLIER_PRICING SP
WHERE P.PRO_ID = SP.PRO_ID AND
O.PRICING_ID = SP.PRICING_ID AND
O.CUS_ID = 2;

/*Display the Supplier details who can supply more than one product.*/
SELECT TEMP.* FROM  (
SELECT S.*,COUNT(S.SUPP_ID) AS TOTAL
FROM SUPPLIER S,SUPPLIER_PRICING SP
where S.SUPP_ID = SP.SUPP_ID
GROUP BY SUPP_ID) TEMP
WHERE TEMP.TOTAL > 1;

/*Find the least expensive product from each category and print the table with category id, name, product name and price of the product*/ 
 SELECT C.CAT_ID,C.CAT_NAME,P.PRO_NAME,MIN(SUPP_PRICE) MINPRICE
 FROM SUPPLIER_PRICING S
 INNER JOIN PRODUCT P ON P.PRO_ID = S.PRO_ID
 INNER JOIN CATEGORY C ON C.CAT_ID = P.CAT_ID
 GROUP BY P.CAT_ID;

/*Display the Id and Name of the Product ordered after “2021-10-05”.*/
SELECT P.PRO_ID,P.PRO_NAME
FROM `ORDER` O,PRODUCT P,SUPPLIER_PRICING SP
WHERE SP.PRICING_ID = O.PRICING_ID AND
SP.PRO_ID = P.PRO_ID AND
O.ORD_DATE > '2021-10-05';

